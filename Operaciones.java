
public class Operaciones {


    public static void main(String[] args) {
        
       int a,b,c;
       a = 10;
       b = 10;
       c = a / b; //Podemos hacer +, -, *, /, 
       
       System.out.println(" Valor de a / b són : " +c);
       
       double d,e,f;
       
       d = 1.5;
       e = 3.0;
       f = d+e;
       
       System.out.println(" Valor de d + e són: " +f);
       f = d - e;
       System.out.println(" Valor de d - e són: " +f);
       f = d*e;
       System.out.println(" Valor de d * e són: " +f);
       f = d / e;
       System.out.println(" Valor de d / e són: " +f);
       f = d % e;
       System.out.println(" Valor de d % e són: " +f);
   
       
      
    }
    
}
